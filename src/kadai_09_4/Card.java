package kadai_09_4;

public class Card {
	
	String suit;
	int number;
	boolean visible;
	
	
	Card(String suit , int number){
		this.suit = suit;
		this.number = number;
		this.visible = false;
	}
	
	
	Card(String suit , int number , boolean visible){
		this.suit = suit;
		this.number = number;
		this.visible = visible;
		
	}
	

	
	
	
	
	

}
